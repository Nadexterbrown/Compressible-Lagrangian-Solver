"""
Piston-driven shock wave verification tests.

Tests single shock waves generated by a piston moving into gas at rest.
Shock strengths are defined by the shock Mach number M_s = 1, 2, 3, 4.

Uses SDToolbox (Shock and Detonation Toolbox) for exact shock calculations
with real gas thermodynamics via Cantera EOS.

For a given shock Mach number M_s:
    Shock speed: D = M_s * c1 (sound speed in undisturbed gas)
    Post-shock state: computed via SDToolbox PostShock_fr (frozen composition)
    Piston velocity: u_p = D * (1 - rho1/rho2) from mass conservation

Uses Cantera EOS with air at STP (298.15 K, 101325 Pa).

Outputs are saved to timestamped subdirectories with:
- config.json: Test configuration for reproducibility
- results.json: Test results summary
- Individual plots for each test showing exact vs numerical

References:
    [Toro2009] Section 4.2 - The Riemann Problem for the Euler Equations
    [Toro2009] Section 6.3.2 - Moving boundaries
    SDToolbox: Shock and Detonation Toolbox (Caltech)
"""

