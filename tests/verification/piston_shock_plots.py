def create_air_eos():
    """
    Create Cantera EOS for air at STP.

    Returns:
        Tuple of (eos, rho, gamma, c) at STP conditions
    """
    # Use GRI-Mech 3.0 which includes air species
    eos = CanteraEOS(mechanism_file="gri30.yaml")

    # Set composition to air (79% N2, 21% O2 by mole)
    eos.gas.X = {"N2": 0.79, "O2": 0.21}

    # Set to STP
    eos.set_state_TP(T_STP, P_STP)

    # Get properties at STP
    rho = eos.gas.density  # kg/m³
    gamma = eos.gas.cp / eos.gas.cv  # ratio of specific heats
    c = eos.gas.sound_speed  # sound speed m/s

    print(f"Air at STP:")
    print(f"  T = {T_STP:.2f} K")
    print(f"  P = {P_STP:.0f} Pa")
    print(f"  rho = {rho:.4f} kg/m^3")
    print(f"  gamma = {gamma:.4f}")
    print(f"  c = {c:.2f} m/s")

    return eos, rho, gamma, c


def compute_shock_speed(M_s: float, c1: float) -> float:
    """
    Compute shock wave speed for a given shock Mach number.

    Args:
        M_s: Shock Mach number
        c1: Sound speed in undisturbed gas [m/s]

    Returns:
        Shock speed [m/s]
    """
    return M_s * c1


def compute_post_shock_state_sdtoolbox(
    M_s: float, T1: float, P1: float, composition: dict, mech: str
) -> dict:
    """
    Compute post-shock state using SDToolbox (real gas thermodynamics).

    Uses SDToolbox PostShock_fr for frozen (non-reactive) shock calculations
    with Cantera EOS.

    Args:
        M_s: Shock Mach number
        T1: Pre-shock temperature [K]
        P1: Pre-shock pressure [Pa]
        composition: Gas composition as dict (e.g., {"N2": 0.79, "O2": 0.21})
        mech: Cantera mechanism file (e.g., "gri30.yaml")

    Returns:
        Dictionary with post-shock state:
            rho1, rho2: Pre/post-shock density [kg/m³]
            p1, p2: Pre/post-shock pressure [Pa]
            T1, T2: Pre/post-shock temperature [K]
            c1, c2: Pre/post-shock sound speed [m/s]
            D: Shock speed [m/s]
            u_p: Piston velocity (= post-shock particle velocity) [m/s]
            gamma1: Pre-shock ratio of specific heats
            e1, e2: Pre/post-shock internal energy [J/kg]

    Reference: SDToolbox documentation, Caltech
    """
    import cantera as ct

    # Create pre-shock gas state
    gas1 = ct.Solution(mech)
    gas1.TPX = T1, P1, composition

    # Get pre-shock properties
    rho1 = gas1.density
    c1 = gas1.sound_speed
    gamma1 = gas1.cp / gas1.cv
    e1 = gas1.int_energy_mass

    # Compute shock speed
    D = M_s * c1

    if M_s <= 1.0:
        # No shock for M_s <= 1
        return {
            "rho1": rho1, "rho2": rho1,
            "p1": P1, "p2": P1,
            "T1": T1, "T2": T1,
            "c1": c1, "c2": c1,
            "D": c1,
            "u_p": 0.0,
            "gamma1": gamma1,
            "e1": e1, "e2": e1,
        }

    # Use SDToolbox to compute post-shock state (frozen composition)
    # PostShock_fr(U1, P1, T1, q, mech) returns gas at post-shock state
    gas2 = PostShock_fr(D, P1, T1, composition, mech)

    # Get post-shock properties
    rho2 = gas2.density
    p2 = gas2.P
    T2 = gas2.T
    c2 = gas2.sound_speed
    e2 = gas2.int_energy_mass

    # Piston velocity from mass conservation across shock:
    # rho1 * D = rho2 * (D - u_p)
    # u_p = D * (1 - rho1/rho2)
    u_p = D * (1.0 - rho1 / rho2)

    return {
        "rho1": rho1, "rho2": rho2,
        "p1": P1, "p2": p2,
        "T1": T1, "T2": T2,
        "c1": c1, "c2": c2,
        "D": D,
        "u_p": u_p,
        "gamma1": gamma1,
        "e1": e1, "e2": e2,
    }


# Legacy functions for backward compatibility (ideal gas approximations)
def compute_pressure_ratio(M_s: float, gamma: float) -> float:
    """Compute pressure ratio using ideal gas Rankine-Hugoniot (legacy)."""
    if M_s <= 1.0:
        return 1.0
    return 1.0 + (2 * gamma / (gamma + 1)) * (M_s**2 - 1)


def compute_piston_velocity_ideal(M_s: float, gamma: float, c1: float) -> float:
    """Compute piston velocity using ideal gas relations (legacy)."""
    if M_s <= 1.0:
        return 0.0
    return c1 * (2 / (gamma + 1)) * (M_s - 1 / M_s)


def exact_piston_solution_sdtoolbox(
    x: np.ndarray,
    t: float,
    x_piston_0: float,
    shock_state: dict,
    ramp_time: float = 30e-6,
) -> tuple:
    """
    Compute exact solution for piston-driven shock at time t using SDToolbox results.

    The piston starts at x_piston_0 and moves right, creating a shock.
    Accounts for velocity ramping during shock formation.

    The piston velocity profile is:
        v(t) = u_p * (t / ramp_time)  for t < ramp_time
        v(t) = u_p                     for t >= ramp_time

    The shock position is computed from mass conservation:
        x_shock = x_piston * D / u_p

    This relationship holds because:
        - Mass swept by shock = rho1 * x_shock
        - Mass compressed behind shock = rho2 * (x_shock - x_piston)
        - Mass conservation: rho1 * x_shock = rho2 * (x_shock - x_piston)
        - Combined with u_p = D * (1 - rho1/rho2) gives x_shock = x_piston * D/u_p

    Args:
        x: Position array [m]
        t: Time [s]
        x_piston_0: Initial piston position [m]
        shock_state: Dictionary from compute_post_shock_state_sdtoolbox containing:
            rho1, rho2, p1, p2, e1, e2, D, u_p
        ramp_time: Time to ramp piston velocity from 0 to u_p [s] (default 30e-6)

    Returns:
        (rho, u, p, e, x_piston, x_shock) arrays/values
    """
    # Extract shock parameters
    rho1 = shock_state["rho1"]
    rho2 = shock_state["rho2"]
    p1 = shock_state["p1"]
    p2 = shock_state["p2"]
    e1 = shock_state["e1"]
    e2 = shock_state["e2"]
    D = shock_state["D"]
    u_p = shock_state["u_p"]

    # Compute piston position with ramped velocity
    # v(t) = u_p * min(t/ramp_time, 1)
    # x_piston = integral of v(t') from 0 to t
    if ramp_time > 0 and t < ramp_time:
        # During ramp: x = integral(u_p * t'/ramp_time, 0, t) = u_p * t^2 / (2*ramp_time)
        x_piston = x_piston_0 + u_p * t**2 / (2 * ramp_time)
    elif ramp_time > 0:
        # After ramp: x = x(ramp_time) + u_p * (t - ramp_time)
        # x(ramp_time) = u_p * ramp_time / 2
        x_piston = x_piston_0 + u_p * ramp_time / 2 + u_p * (t - ramp_time)
        # Simplify: x_piston = x_piston_0 + u_p * (t - ramp_time/2)
    else:
        # No ramp
        x_piston = x_piston_0 + u_p * t

    # Shock position from mass conservation: x_shock = x_piston * D / u_p
    # This holds because u_p = D * (1 - rho1/rho2)
    if u_p > 0:
        x_shock = x_piston_0 + (x_piston - x_piston_0) * D / u_p
    else:
        x_shock = x_piston_0  # No shock for u_p = 0

    # Current piston velocity (accounts for ramp)
    if ramp_time > 0 and t < ramp_time:
        u_piston_current = u_p * t / ramp_time
    else:
        u_piston_current = u_p

    # Initialize with pre-shock state
    rho = np.full_like(x, rho1)
    u = np.zeros_like(x)
    p = np.full_like(x, p1)
    e = np.full_like(x, e1)

    # Post-shock region: between piston and shock
    # Note: During ramp, this is an approximation - the shock is still forming
    # and the post-shock state varies. After ramp, it's the steady shock solution.
    post_shock = (x >= x_piston) & (x < x_shock)
    rho[post_shock] = rho2
    u[post_shock] = u_piston_current  # Post-shock velocity = current piston velocity
    p[post_shock] = p2
    e[post_shock] = e2

    return rho, u, p, e, x_piston, x_shock


# Legacy function for backward compatibility
def exact_piston_solution(
    x: np.ndarray,
    t: float,
    x_piston_0: float,
    M_s: float,
    gamma: float,
    rho1: float,
    p1: float,
) -> tuple:
    """
    Compute exact solution using ideal gas Rankine-Hugoniot (legacy).

    Kept for backward compatibility. Use exact_piston_solution_sdtoolbox
    for real gas calculations.
    """
    c1 = np.sqrt(gamma * p1 / rho1)

    # Compute shock and piston speeds
    D = compute_shock_speed(M_s, c1)
    u_p = compute_piston_velocity_ideal(M_s, gamma, c1)

    # Current positions
    x_piston = x_piston_0 + u_p * t
    x_shock = x_piston_0 + D * t

    # Post-shock state (ideal gas)
    P = compute_pressure_ratio(M_s, gamma)
    p2 = P * p1
    rho_ratio = ((gamma + 1) * P + (gamma - 1)) / ((gamma - 1) * P + (gamma + 1))
    rho2 = rho1 * rho_ratio
    u2 = u_p

    # Initialize with pre-shock state
    rho = np.full_like(x, rho1)
    u = np.zeros_like(x)
    p = np.full_like(x, p1)

    # Post-shock region: between piston and shock
    post_shock = (x >= x_piston) & (x < x_shock)
    rho[post_shock] = rho2
    u[post_shock] = u2
    p[post_shock] = p2

    # Internal energy
    e = p / (rho * (gamma - 1))

    return rho, u, p, e, x_piston, x_shock


